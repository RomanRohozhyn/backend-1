package Praktika_22_08_24;

public class Main {
    public static void main(String[] args) {

        int i = 1;
        int n = 10;

        while (i < n)
        {
            int j = 0;
            while (j < i)
            {
                System.out.print(i);
                j++;
            }
            i++;
            System.out.println();

        }

    }

}
