package Praktika_23_08_24;

public class NeuFigure {

    public static void main(String[] args) {

        int i = 0;
        int n = 10;

        while (i < n)
        {
            int j = 0;

            while (j <= i)
            {
                System.out.print(i);
                j++;
            }
            System.out.println();
            i++;
        }


    }
}